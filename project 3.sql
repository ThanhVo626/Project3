CREATE TABLE Zipcode (
    DataYear INT,
    DashboardFuelTypeGroup VARCHAR(255),
    FuelType VARCHAR(255),
    Zip VARCHAR(10),
    NumberOfVehicles INT
);

CREATE TABLE Electricvehicles (
    DataYear INT,
    County VARCHAR(255),
    DashboardFuelTypeGroup VARCHAR(255),
    FuelType VARCHAR(255),
    Manufacturer VARCHAR(255),
    Model VARCHAR(255),
    NumberOfVehicles INT
);


